def bmp_info(data):
    useful = struct.unpack('<ccIIIIIIHH', data[:30])
    if useful[0]==b'B' and useful[1]==b'M':
        return{
            'width':useful[6],
            'height':useful[7],
            'color':useful[9],
        }
    else:
        print('NotBmp')

